var target = []rune("XMAS")

var directions = [][]int{
	{0, 1}, {1, 1}, {1, 0}, {-1, -1}, {-1, 0}, {0, -1}, {1, -1}, {-1, 1}}

func main() {
	got := getAllMatches(getInput(input))
	fmt.Printf("part1 matches: %v\n", got)

	got = getAllMatchesP2(getInput(input))
	fmt.Printf("part2 matches: %v\n", got)
}

func getInput(in string) [][]rune {
	r := [][]rune{}
	lines := strings.Split(in, "\n")
	for i := range lines {
		r = append(r, []rune(lines[i]))
	}
	return r
}

func getAllMatches(in [][]rune) int {
	count := 0
	for i, line := range in {
		for j, letter := range line {
			if letter == target[0] {
				for d := range directions {
					count += matchesWord(i, j, in, directions[d], 1)
				}
			}
		}
	}
	return count
}

func matchesWord(i, j int, in [][]rune, direction []int, distance int) int {
	ci := i + (direction[0] * distance)
	cj := j + (direction[1] * distance)
	if cj < 0 || ci >= len(in) || ci < 0 || cj >= len(in[0]) {
		return 0
	}

	c := in[ci][cj]
	t := target[distance]

	if c != t {
		return 0
	}

	if distance == len(target)-1 {
		return 1
	}
	return matchesWord(i, j, in, direction, distance+1)
}

func getAllMatchesP2(in [][]rune) int {
	count := 0
	for i, line := range in {
		for j, letter := range line {
			if letter == rune('A') {
				count += matchesXShape(i, j, in)
			}
		}
	}
	return count
}

func inRange(r []int, in [][]rune) bool {
	if r[1] < 0 || r[0] >= len(in) || r[0] < 0 || r[1] >= len(in[0]) {
		return false
	}
	return true
}

func matchesXShape(i, j int, in [][]rune) int {
	p1a := []int{i - 1, j - 1}
	p1b := []int{i + 1, j + 1}

	p2a := []int{i - 1, j + 1}
	p2b := []int{i + 1, j - 1}

	if !(inRange(p1a, in) && inRange(p1b, in) && inRange(p2a, in) && inRange(p2b, in)) {
		return 0
	}

	p1ok := false
	if in[p1a[0]][p1a[1]] == rune('M') {
		if in[p1b[0]][p1b[1]] == rune('S') {
			p1ok = true
		}
	} else {
		if in[p1a[0]][p1a[1]] == rune('S') {
			if in[p1b[0]][p1b[1]] == rune('M') {
				p1ok = true
			}
		}
	}

	p2ok := false
	if in[p2a[0]][p2a[1]] == rune('M') {
		if in[p2b[0]][p2b[1]] == rune('S') {
			p2ok = true
		}
	} else {
		if in[p2a[0]][p2a[1]] == rune('S') {
			if in[p2b[0]][p2b[1]] == rune('M') {
				p2ok = true
			}
		}
	}
	if p1ok && p2ok {
		return 1
	}
	return 0
}

